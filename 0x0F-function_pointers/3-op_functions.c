#include "3-calc.h"

/**
 * op_add - Operator function for addition
 * @a: input integer
 * @b: output integer
 *
 * Return: Result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Operator function for subtraction
 * @a: input integer
 * @b: input integer
 *
 * Return: Result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Operator function for multiplication
 * @a: input integer
 * @b: input intger
 *
 * Return: Result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Operator function for division
 * @a: input integer
 * @b: input integer
 *
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Operator function for calculating remainders
 * @a: input integer
 * @b: input integer
 *
 * Return: Result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
